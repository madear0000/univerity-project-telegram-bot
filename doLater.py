def getScheduledTasks(current_time):
    conn = sqlite3.connect("tasksForDoLater.db", check_same_thread=False)
    cursor = conn.cursor()
    cursor.execute("SELECT user_id, task_name, reminder_date FROM tasks WHERE reminder_date = ?", (current_time,))
